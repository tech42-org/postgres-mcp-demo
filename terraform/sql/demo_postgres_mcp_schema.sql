WITH order_seed AS (
    SELECT
        i,
        c.id AS customer_id,
        c.tenant_id,
        (current_date - ((i % 180) || ' days')::interval + ((i % 24) || ' hours')::interval) AS order_ts,
        (ARRAY['paid', 'paid', 'paid', 'paid', 'refunded', 'cancelled'])[1 + (i % 6)] AS status,
        round((35 + (i % 250) * 4.65)::numeric, 2) AS subtotal,
        round(((i % 4) * 3.25)::numeric, 2) AS discount,
        round((3 + (i % 30) * 0.58)::numeric, 2) AS tax,
        round((5 + (i % 7) * 1.15)::numeric, 2) AS shipping
    FROM generate_series(1, 640 * :seed_scale_factor) AS g(i)
