SELECT format('CREATE ROLE %I LOGIN PASSWORD %L NOSUPERUSER NOCREATEDB NOCREATEROLE NOINHERIT NOBYPASSRLS', :'raw_role', :'raw_role_password')
WHERE NOT EXISTS (SELECT 1 FROM pg_roles WHERE rolname = :'raw_role') \gexec
SELECT format('CREATE ROLE %I LOGIN PASSWORD %L NOSUPERUSER NOCREATEDB NOCREATEROLE NOINHERIT NOBYPASSRLS', :'views_role', :'views_role_password')
WHERE NOT EXISTS (SELECT 1 FROM pg_roles WHERE rolname = :'views_role') \gexec

-- RDS master users cannot alter SUPERUSER/BYPASSRLS attributes. New roles are
-- created least-privilege above; reruns only need to refresh passwords.
