ALTER ROLE :"views_role" WITH LOGIN PASSWORD :'views_role_password';

CREATE SCHEMA IF NOT EXISTS raw_app;
CREATE SCHEMA IF NOT EXISTS analytics_app;
CREATE SCHEMA IF NOT EXISTS demo_app;

CREATE TABLE IF NOT EXISTS raw_app.tenants (
    id text PRIMARY KEY,
    name text NOT NULL,
    region text NOT NULL,
    created_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS raw_app.customer_segments (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    tenant_id text NOT NULL REFERENCES raw_app.tenants(id),
    name text NOT NULL,
    margin_tier text NOT NULL,
    lifecycle_stage text NOT NULL,
    UNIQUE (tenant_id, name)
);

CREATE TABLE IF NOT EXISTS raw_app.customers (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    tenant_id text NOT NULL REFERENCES raw_app.tenants(id),
    segment_id integer NOT NULL REFERENCES raw_app.customer_segments(id),
    external_ref text NOT NULL,
    full_name text NOT NULL,
    email text NOT NULL,
    phone text,
    acquisition_channel text NOT NULL,
    signup_date date NOT NULL,
    status text NOT NULL,
    UNIQUE (tenant_id, email)
);

CREATE TABLE IF NOT EXISTS raw_app.customer_addresses (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    customer_id integer NOT NULL REFERENCES raw_app.customers(id),
    address_type text NOT NULL,
    city text NOT NULL,
    state text NOT NULL,
    country text NOT NULL,
    postal_code text NOT NULL,
    is_default boolean NOT NULL DEFAULT false
);

CREATE TABLE IF NOT EXISTS raw_app.categories (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    parent_id integer REFERENCES raw_app.categories(id),
    name text NOT NULL,
    merchandising_owner text NOT NULL
);

CREATE TABLE IF NOT EXISTS raw_app.suppliers (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name text NOT NULL,
    country text NOT NULL,
    reliability_score numeric(5,2) NOT NULL
);

CREATE TABLE IF NOT EXISTS raw_app.products (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    category_id integer NOT NULL REFERENCES raw_app.categories(id),
    supplier_id integer NOT NULL REFERENCES raw_app.suppliers(id),
    sku text NOT NULL UNIQUE,
    name text NOT NULL,
    brand text NOT NULL,
    list_price numeric(12,2) NOT NULL,
    standard_cost numeric(12,2) NOT NULL,
    active boolean NOT NULL DEFAULT true
);

CREATE TABLE IF NOT EXISTS raw_app.warehouses (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    code text NOT NULL UNIQUE,
    region text NOT NULL,
    city text NOT NULL
);

CREATE TABLE IF NOT EXISTS raw_app.inventory_snapshots (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    product_id integer NOT NULL REFERENCES raw_app.products(id),
    warehouse_id integer NOT NULL REFERENCES raw_app.warehouses(id),
    snapshot_date date NOT NULL,
    on_hand integer NOT NULL,
    allocated integer NOT NULL,
    reorder_point integer NOT NULL,
    UNIQUE (product_id, warehouse_id, snapshot_date)
);

CREATE TABLE IF NOT EXISTS raw_app.orders (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    tenant_id text NOT NULL REFERENCES raw_app.tenants(id),
    customer_id integer NOT NULL REFERENCES raw_app.customers(id),
    order_number text NOT NULL UNIQUE,
    order_ts timestamptz NOT NULL,
    status text NOT NULL,
    currency text NOT NULL,
    subtotal_amount numeric(12,2) NOT NULL,
    discount_amount numeric(12,2) NOT NULL,
    tax_amount numeric(12,2) NOT NULL,
    shipping_amount numeric(12,2) NOT NULL,
    total_amount numeric(12,2) NOT NULL
);

CREATE TABLE IF NOT EXISTS raw_app.order_lines (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    order_id integer NOT NULL REFERENCES raw_app.orders(id),
    product_id integer NOT NULL REFERENCES raw_app.products(id),
    quantity integer NOT NULL,
    unit_price numeric(12,2) NOT NULL,
    unit_cost numeric(12,2) NOT NULL,
    discount_amount numeric(12,2) NOT NULL
);

CREATE TABLE IF NOT EXISTS raw_app.payments (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    order_id integer NOT NULL REFERENCES raw_app.orders(id),
    payment_ts timestamptz NOT NULL,
    payment_method text NOT NULL,
    status text NOT NULL,
    processor text NOT NULL,
    amount numeric(12,2) NOT NULL
);

CREATE TABLE IF NOT EXISTS raw_app.shipments (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    order_id integer NOT NULL REFERENCES raw_app.orders(id),
    warehouse_id integer NOT NULL REFERENCES raw_app.warehouses(id),
    carrier text NOT NULL,
    shipped_ts timestamptz,
    delivered_ts timestamptz,
    status text NOT NULL,
    shipping_cost numeric(12,2) NOT NULL
);

CREATE TABLE IF NOT EXISTS raw_app.reviews (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    tenant_id text NOT NULL REFERENCES raw_app.tenants(id),
    customer_id integer NOT NULL REFERENCES raw_app.customers(id),
    product_id integer NOT NULL REFERENCES raw_app.products(id),
    rating integer NOT NULL CHECK (rating BETWEEN 1 AND 5),
    review_ts timestamptz NOT NULL,
    sentiment text NOT NULL
);

CREATE TABLE IF NOT EXISTS raw_app.promotions (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    code text NOT NULL UNIQUE,
    channel text NOT NULL,
    starts_on date NOT NULL,
    ends_on date NOT NULL,
    discount_type text NOT NULL
);

CREATE TABLE IF NOT EXISTS raw_app.order_promotions (
    order_id integer NOT NULL REFERENCES raw_app.orders(id),
    promotion_id integer NOT NULL REFERENCES raw_app.promotions(id),
    discount_amount numeric(12,2) NOT NULL,
    PRIMARY KEY (order_id, promotion_id)
);

CREATE TABLE IF NOT EXISTS raw_app.support_tickets (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    tenant_id text NOT NULL REFERENCES raw_app.tenants(id),
    customer_id integer NOT NULL REFERENCES raw_app.customers(id),
    order_id integer REFERENCES raw_app.orders(id),
    opened_ts timestamptz NOT NULL,
    closed_ts timestamptz,
    priority text NOT NULL,
    topic text NOT NULL,
    status text NOT NULL
);

CREATE TABLE IF NOT EXISTS raw_app.ticket_events (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ticket_id integer NOT NULL REFERENCES raw_app.support_tickets(id),
    event_ts timestamptz NOT NULL,
    event_type text NOT NULL,
    actor_type text NOT NULL
);

CREATE TABLE IF NOT EXISTS raw_app.marketing_events (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    tenant_id text NOT NULL REFERENCES raw_app.tenants(id),
    customer_id integer REFERENCES raw_app.customers(id),
    event_ts timestamptz NOT NULL,
    campaign text NOT NULL,
    channel text NOT NULL,
    event_type text NOT NULL
);

CREATE TABLE IF NOT EXISTS raw_app.web_sessions (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    tenant_id text NOT NULL REFERENCES raw_app.tenants(id),
    customer_id integer REFERENCES raw_app.customers(id),
    started_ts timestamptz NOT NULL,
    device_type text NOT NULL,
    landing_page text NOT NULL,
    converted_order_id integer REFERENCES raw_app.orders(id)
);

