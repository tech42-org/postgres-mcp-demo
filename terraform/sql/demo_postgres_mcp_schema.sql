CREATE OR REPLACE VIEW analytics_app.customer_revenue_summary AS
WITH order_rollup AS (
    SELECT
        customer_id,
        count(*) AS order_count,
        min(order_ts)::date AS first_order_date,
        max(order_ts)::date AS most_recent_order_date,
        sum(total_amount) AS gross_revenue,
        avg(total_amount) AS average_order_value
    FROM raw_app.orders
    WHERE status IN ('paid', 'refunded')
    GROUP BY customer_id
),
payment_rollup AS (
    SELECT
        o.customer_id,
        sum(CASE WHEN p.status = 'completed' THEN p.amount ELSE 0 END) AS total_paid_amount,
        count(*) FILTER (WHERE p.status = 'failed') AS failed_payment_count
    FROM raw_app.orders AS o
    JOIN raw_app.payments AS p ON p.order_id = o.id
    GROUP BY o.customer_id
),
review_rollup AS (
    SELECT customer_id, count(*) AS review_count, avg(rating) AS average_rating
    FROM raw_app.reviews
    GROUP BY customer_id
)
SELECT
    c.tenant_id,
    c.id AS customer_id,
    c.full_name AS customer_name,
    c.email,
    cs.name AS segment_name,
    c.acquisition_channel,
    c.signup_date,
    coalesce(o.order_count, 0) AS order_count,
    o.first_order_date,
    o.most_recent_order_date,
    coalesce(o.gross_revenue, 0) AS gross_revenue,
    coalesce(o.average_order_value, 0) AS average_order_value,
    coalesce(p.total_paid_amount, 0) AS total_paid_amount,
    coalesce(p.failed_payment_count, 0) AS failed_payment_count,
    coalesce(r.review_count, 0) AS review_count,
    coalesce(r.average_rating, 0) AS average_rating
FROM raw_app.customers AS c
JOIN raw_app.customer_segments AS cs ON cs.id = c.segment_id
LEFT JOIN order_rollup AS o ON o.customer_id = c.id
LEFT JOIN payment_rollup AS p ON p.customer_id = c.id
LEFT JOIN review_rollup AS r ON r.customer_id = c.id
WHERE c.tenant_id = current_setting('app.tenant_id', true);

CREATE OR REPLACE VIEW analytics_app.daily_sales_summary AS
SELECT
    tenant_id,
    order_ts::date AS order_date,
    count(*) AS order_count,
    sum(total_amount) AS gross_revenue,
    avg(total_amount) AS average_order_value,
    count(*) FILTER (WHERE status = 'cancelled') AS cancelled_order_count,
    count(*) FILTER (WHERE status = 'refunded') AS refunded_order_count
FROM raw_app.orders
WHERE tenant_id = current_setting('app.tenant_id', true)
GROUP BY tenant_id, order_ts::date;

CREATE OR REPLACE VIEW analytics_app.product_sales_summary AS
SELECT
    o.tenant_id,
    p.id AS product_id,
    p.sku,
    p.name AS product_name,
    cat.name AS category_name,
    s.name AS supplier_name,
    sum(ol.quantity) AS units_sold,
    sum(ol.quantity * ol.unit_price - ol.discount_amount) AS net_revenue,
    sum(ol.quantity * (ol.unit_price - ol.unit_cost) - ol.discount_amount) AS gross_margin,
    avg(r.rating) AS average_rating,
    count(r.id) AS review_count
FROM raw_app.order_lines AS ol
JOIN raw_app.orders AS o ON o.id = ol.order_id
JOIN raw_app.products AS p ON p.id = ol.product_id
JOIN raw_app.categories AS cat ON cat.id = p.category_id
JOIN raw_app.suppliers AS s ON s.id = p.supplier_id
LEFT JOIN raw_app.reviews AS r ON r.product_id = p.id AND r.tenant_id = o.tenant_id
WHERE o.tenant_id = current_setting('app.tenant_id', true)
GROUP BY o.tenant_id, p.id, p.sku, p.name, cat.name, s.name;

CREATE OR REPLACE VIEW analytics_app.monthly_product_sales_summary AS
SELECT
    o.tenant_id,
    date_trunc('month', o.order_ts)::date AS order_month,
    p.id AS product_id,
    p.sku,
    p.name AS product_name,
    cat.name AS category_name,
    s.name AS supplier_name,
    count(DISTINCT o.id) AS order_count,
    sum(ol.quantity) AS units_sold,
    sum(ol.quantity * ol.unit_price - ol.discount_amount) AS net_revenue,
    sum(ol.quantity * (ol.unit_price - ol.unit_cost) - ol.discount_amount) AS gross_margin,
    avg(ol.unit_price) AS average_unit_price
FROM raw_app.order_lines AS ol
JOIN raw_app.orders AS o ON o.id = ol.order_id
JOIN raw_app.products AS p ON p.id = ol.product_id
JOIN raw_app.categories AS cat ON cat.id = p.category_id
JOIN raw_app.suppliers AS s ON s.id = p.supplier_id
WHERE o.tenant_id = current_setting('app.tenant_id', true)
GROUP BY o.tenant_id, date_trunc('month', o.order_ts)::date, p.id, p.sku, p.name, cat.name, s.name;

CREATE OR REPLACE VIEW analytics_app.category_performance AS
SELECT
    tenant_id,
    category_name,
    count(DISTINCT product_id) AS product_count,
    sum(units_sold) AS units_sold,
    sum(net_revenue) AS net_revenue,
    sum(gross_margin) AS gross_margin,
    CASE WHEN sum(net_revenue) = 0 THEN 0 ELSE sum(gross_margin) / sum(net_revenue) END AS gross_margin_rate
FROM analytics_app.product_sales_summary
WHERE tenant_id = current_setting('app.tenant_id', true)
GROUP BY tenant_id, category_name;

CREATE OR REPLACE VIEW analytics_app.payment_health_daily AS
SELECT
    o.tenant_id,
    p.payment_ts::date AS payment_date,
    p.payment_method,
    count(*) AS payment_count,
    sum(p.amount) AS payment_amount,
    count(*) FILTER (WHERE p.status = 'failed') AS failed_payment_count,
    CASE WHEN count(*) = 0 THEN 0 ELSE count(*) FILTER (WHERE p.status = 'failed')::numeric / count(*) END AS failed_payment_rate
FROM raw_app.payments AS p
JOIN raw_app.orders AS o ON o.id = p.order_id
WHERE o.tenant_id = current_setting('app.tenant_id', true)
GROUP BY o.tenant_id, p.payment_ts::date, p.payment_method;

CREATE OR REPLACE VIEW analytics_app.fulfillment_health_daily AS
SELECT
    o.tenant_id,
    o.order_ts::date AS order_date,
    s.carrier,
    count(*) AS shipment_count,
    count(*) FILTER (WHERE s.status = 'delayed') AS delayed_shipment_count,
    avg(extract(epoch FROM (s.delivered_ts - s.shipped_ts)) / 86400.0) AS average_delivery_days,
    sum(s.shipping_cost) AS shipping_cost
FROM raw_app.shipments AS s
JOIN raw_app.orders AS o ON o.id = s.order_id
WHERE o.tenant_id = current_setting('app.tenant_id', true)
GROUP BY o.tenant_id, o.order_ts::date, s.carrier;

CREATE OR REPLACE VIEW analytics_app.customer_support_summary AS
SELECT
    tenant_id,
    customer_id,
    count(*) AS ticket_count,
    count(*) FILTER (WHERE status = 'open') AS open_ticket_count,
    count(*) FILTER (WHERE priority IN ('high', 'urgent')) AS high_priority_ticket_count,
    avg(extract(epoch FROM (closed_ts - opened_ts)) / 3600.0) AS average_resolution_hours
FROM raw_app.support_tickets
WHERE tenant_id = current_setting('app.tenant_id', true)
GROUP BY tenant_id, customer_id;

CREATE OR REPLACE VIEW analytics_app.executive_kpis AS
SELECT
    d.tenant_id,
    sum(d.gross_revenue) AS gross_revenue,
    sum(d.order_count) AS order_count,
    avg(d.average_order_value) AS average_order_value,
    sum(p.failed_payment_count) AS failed_payment_count,
    avg(p.failed_payment_rate) AS average_failed_payment_rate,
    sum(f.delayed_shipment_count) AS delayed_shipment_count
FROM analytics_app.daily_sales_summary AS d
LEFT JOIN analytics_app.payment_health_daily AS p
  ON p.tenant_id = d.tenant_id
 AND p.payment_date = d.order_date
LEFT JOIN analytics_app.fulfillment_health_daily AS f
  ON f.tenant_id = d.tenant_id
 AND f.order_date = d.order_date
WHERE d.tenant_id = current_setting('app.tenant_id', true)
GROUP BY d.tenant_id;

